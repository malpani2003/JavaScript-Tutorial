var x=3;
switch (x) {
    case 1:
        console.log("x is 1");
        break;
    case 2:
        console.log("x is 2");
        break;
    case 3:
        console.log("x is 3");
        break;
    case 4:
        console.log("x is 4");
        break;
    default:
        console.log("Number is not 1,2,3,4")
        break;
}