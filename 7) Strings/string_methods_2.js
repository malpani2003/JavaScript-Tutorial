let string="Javascript is a great language";

// charAt()- find char at position
console.log("Find char : " + string.charAt(5));

// charCodeAt() - return ascii code of char at position
console.log("Find char : " + string.charCodeAt(5));

// split()- string ko seaprator ke anusar separte kar deta hai
console.log("Split String : "+string.split(" "));
console.log("Split String : "+string.split("i"));

// repeat() - repeat string number of times
console.log("reapeat : "+string.repeat(4))

// slice()
console.log("Slice : "+string.slice(3,23));

// substring() - similar to slice()
console.log("Substring : "+ string.substring(3,23));



